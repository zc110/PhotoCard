package com.example.photo;

public class Photo {
	int ImageHead;
	String name;
	String time;
	String text;
	String zuihou;
	String future;
	int lastCard;

	


	public int getImageHead() {
		return ImageHead;
	}

	public void setImageHead(int imageHead) {
		ImageHead = imageHead;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getZuihou() {
		return zuihou;
	}

	public void setZuihou(String zuihou) {
		this.zuihou = zuihou;
	}

	public String getFuture() {
		return future;
	}

	public void setFuture(String future) {
		this.future = future;
	}

	public int getLastCard() {
		return lastCard;
	}

	public void setLastCard(int lastCard) {
		this.lastCard = lastCard;
	}

	
}
